 import java.lang.Math;
 class BuildIn{
    //build in functgion are the pre defined function already made by java
    public static void main(String[] args) {
        //using the pre-define function called Math.sin
        double sine=Math.sin(1.2);
        System.out.println("The sin of 1.2 is :"+sine);
        
        //using the pre-define function called Math.pow
        double power=Math.pow(2, 3);
        System.out.println("2 to the power 3 is :"+power);
        
    }
 }
